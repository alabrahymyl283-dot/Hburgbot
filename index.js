function fetchJSON(url, options = {}) {
  return new Promise((resolve, reject) => {
    const lib = url.startsWith("https") ? https : http;
    const req = lib.request(url, { method: options.method || "GET", headers: options.headers || {} }, (res) => {
      let data = "";
      res.on("data", chunk => data += chunk);
      res.on("end", () => {
        try { resolve(JSON.parse(data)); }
        catch (e) { reject(new Error("parse error")); }
      });
    });
    req.on("error", reject);
    if (options.body) req.write(options.body);
    req.end();
  });
}

async function getUpdates() {
  const url = `https://api.telegram.org/bot${BOT_TOKEN}/getUpdates?timeout=10&offset=${offset}&allowed_updates=["message"]`;
  return fetchJSON(url);
}

async function sendMessage(chatId, text) {
  const url = `https://api.telegram.org/bot${BOT_TOKEN}/sendMessage`;
  return fetchJSON(url, {
    method: "POST",
    headers: { "Content-Type": "application/json" },
    body: JSON.stringify({ chat_id: chatId, text })
  });
}

function getReply() {
  const allNames = [...boysNames, ...girlsNames];
  const name = allNames[Math.floor(Math.random() * allNames.length)];
  const template = replies[Math.floor(Math.random() * replies.length)];
  return template.replace(/NAME/g, name);
}

async function poll() {
  try {
    const data = await getUpdates();
    if (!data.ok) {
      console.error("Telegram error:", data.description);
      setTimeout(poll, 3000);
      return;
    }
    for (const update of data.result) {
      offset = update.update_id + 1;
      const msg = update.message;
      if (!msg || !msg.text || msg.from?.is_bot) continue;
      const chatId = msg.chat.id;
      const text = msg.text;
      console.log(`[MSG] ${text}`);
      const reply = getReply();
      await sendMessage(chatId, reply);
      console.log(`[REPLY] ${reply}`);
    }
  } catch (e) {
    console.error("[POLL ERROR]", e.message);
  }
  setTimeout(poll, 1500);
}

